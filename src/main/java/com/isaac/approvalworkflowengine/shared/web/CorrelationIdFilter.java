package com.isaac.approvalworkflowengine.shared.web;

import com.isaac.approvalworkflowengine.shared.context.CorrelationIdContext;
import jakarta.servlet.FilterChain;
import jakarta.servlet.ServletException;
import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;
import java.io.IOException;
import java.util.UUID;
import org.slf4j.MDC;
import org.springframework.core.Ordered;
import org.springframework.core.annotation.Order;
import org.springframework.stereotype.Component;
import org.springframework.util.StringUtils;
import org.springframework.web.filter.OncePerRequestFilter;

/**
 * Ensures every HTTP request has a correlation id available in MDC and response headers.
 */
@Component
@Order(Ordered.HIGHEST_PRECEDENCE)
public class CorrelationIdFilter extends OncePerRequestFilter {

    @Override
    protected void doFilterInternal(HttpServletRequest request, HttpServletResponse response, FilterChain filterChain)
        throws ServletException, IOException {

        String incoming = request.getHeader(CorrelationIdContext.HEADER_NAME);
        String correlationId = StringUtils.hasText(incoming) ? incoming : UUID.randomUUID().toString();

        request.setAttribute(CorrelationIdContext.REQUEST_ATTRIBUTE, correlationId);
        response.setHeader(CorrelationIdContext.HEADER_NAME, correlationId);
        MDC.put(CorrelationIdContext.MDC_KEY, correlationId);

        try {
            filterChain.doFilter(request, response);
        } finally {
            MDC.remove(CorrelationIdContext.MDC_KEY);
        }
    }
}
